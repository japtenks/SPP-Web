function redirect($linkto,$type=0,$wait_sec=0){
    if($linkto){
        if($type==0){
            $GLOBALS['redirect'] = '<meta http-equiv=refresh content="'.$wait_sec.';url='.$linkto.'">';
        }else{
            // Use an HTTP redirect when possible, but fall back to JS/meta
            // if output has already started.
            if (!headers_sent()) {
                header("Location: ".$linkto);
            } else {
                $safeLink = htmlspecialchars($linkto, ENT_QUOTES, 'UTF-8');
                echo '<script>window.location.href=' . json_encode($linkto) . ';</script>';
                echo '<noscript><meta http-equiv="refresh" content="0;url=' . $safeLink . '"></noscript>';
            }
        }
    }
}

/**
 * Returns an English resource file.
 *
 * This keeps legacy resource lookup paths working while the site remains
 * English-first.
 *
 * @param $name string The resource name in "filename.ext" format
 * @return string Translated resource content.
 */
function lang_resource($name){
    $resourceinfo = pathinfo($name);
    $resourcename = $resourceinfo["filename"];
    $resourceext = $resourceinfo["extension"];

    $sourcefiles = array(
        "lang/$resourcename/en.$resourceext",
        "lang/$resourcename/{$resourcename}_en.$resourceext",
        "lang/en.$resourcename.$resourceext",
    );

    foreach($sourcefiles as $sourcefile)
    {
        if (!file_exists($sourcefile)) {
            continue;
        }
        $result = file_get_contents($sourcefile);
        if (trim($result) !== '') {
            return $result;
        }
    }
    return '';
}


